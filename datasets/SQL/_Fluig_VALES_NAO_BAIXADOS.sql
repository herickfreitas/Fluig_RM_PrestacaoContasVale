CREATE VIEW [dbo].[_Fluig_VALES_NAO_BAIXADOS]

AS

SELECT 
FLAN.CODFILIAL,
FLAN.CODTDO,
FTDO.DESCRICAO,
Convert(varchar(10), FLAN.DATAEMISSAO,103) DATAEMISSAO,
FLAN.NUMERODOCUMENTO, 
FCFO.NOMEFANTASIA, 
FORMAT(FLAN.VALORORIGINAL , 'C', 'pt-br') VALORORIGINAL,
'FAVORECIDO: '+FCFO.NOMEFANTASIA +' - Nº DOC.: '+ FLAN.NUMERODOCUMENTO +' - '+FORMAT(FLAN.VALORORIGINAL , 'C', 'pt-br') as CONCATENANDO

FROM  FLAN(NOLOCK) 
			 JOIN FCFO (NOLOCK)		ON(FLAN.CODCFO=FCFO.CODCFO)
			 JOIN FTDO (NOLOCK)		ON(FLAN.CODTDO=FTDO.CODTDO)
WHERE 	FLAN.CODCOLIGADA 		= 	1
AND   	FLAN.CODTDO 			IN 	('11') 
AND		FLAN.STATUSLAN			in 	( 0, 4) /* 0 = Em Aberto | 1 = Baixado | 2 = Cancelado | 3 = Baixado por Acordo | 4 = Baixado parcialmente | 5 = Borderô  */


GO


